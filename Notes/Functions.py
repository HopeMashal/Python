#!""" Functions """

""" To create function use { def function_name(): } """
def say_hi():
  print("Hi There!!")
  print("Enjoy")

say_hi()   #* The output is Hi There!!  \n  Enjoy

""" Function with parameter  """
def print_name(name):
  print("Hello " + name)

print_name("Hope!!!")   #* The output is Hello Hope!!!

""" Cube Function """
def cube(num):
  return pow(num,3)

print(cube(3))  #* The output is 27

""" Power Function """
def power(base_num,pow_num):
  return pow(base_num,pow_num)
print(power(3,4))   #* The output is 81

